
#Indentation means the spaces at the beginning of a line of code.
#In Python, indentation is not optional — it is used to define code blocks.
#If a line ends with a colon : → the next line must be indented.

#eg-
marks=int(input("enter marks: "))
if marks > 40:      # Colon → indent starts
    print("Pass")   # Indented

print("Done")       # No colon above → no indent
